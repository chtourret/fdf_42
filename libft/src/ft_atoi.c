#include "../include/libft.h"

int		ft_atoi(const char *str)
{
	int nb;
	int i;
	int neg;

	nb = 0;
	i = 0;
	neg = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')
		i++;
	if (str[i] == '-')
		neg = 1;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		nb = nb * 10 + str[i] - 48;
		i++;
	}
	if (neg == 1)
		return (nb * -1);
	else
		return (nb);
}
